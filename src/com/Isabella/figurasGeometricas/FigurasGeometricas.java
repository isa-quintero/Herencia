package com.Isabella.figurasGeometricas;

public class FigurasGeometricas {
    private float perimetro;
    private float area;
    private byte numeroDeLineas;
    private byte numeroDimensiones;

    public float getPerimetro() {
        return perimetro;
    }

    public void setPerimetro(float perimetro) {
        this.perimetro = perimetro;
    }

    public float getArea() {
        return area;
    }

    public void setArea(float area) {
        this.area = area;
    }

    public byte getNumeroDeLineas() {
        return numeroDeLineas;
    }

    public void setNumeroDeLineas(byte numeroDeLineas) {
        this.numeroDeLineas = numeroDeLineas;
    }

    public byte getNumeroDimensiones() {
        return numeroDimensiones;
    }

}
